package net.chuangdie.lhb.bean;

public class Person
{
	private String name;
	private String sex;
	private int iphone;

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public String getSex()
	{
		return sex;
	}

	public void setSex(String sex)
	{
		this.sex = sex;
	}

	public int getIphone()
	{
		return iphone;
	}

	public void setIphone(int iphone)
	{
		this.iphone = iphone;
	}

}
